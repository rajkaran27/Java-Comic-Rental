    private void btnSearch1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearch1ActionPerformed
        //get input from search bar
        String Input = txtSearch.getText();

        if (radioISBN.isSelected())
        {   
            //search by ISBN
            Comic ComicFound = RS.SearchBook(Input);

            

            if (ComicFound == null)
            {
                txtMessage.setText("No such ISBN \"" + Input + "\" in the data!");
            } else
            {

                int index = RS.getComicIndex(Input);
                int size = RS.getSizeOfComic();
                index += 1;
                lblComic.setText("Comic " + index + " of " + size);
                txtISBN.setText(ComicFound.getISBN());
                txtTitle.setText(ComicFound.getTitle());
                txtRentalCost.setText(ComicFound.getRentalFee());
                txtDeposit.setText(ComicFound.getDepositFee());
                txtInfo.setText(ComicFound.getInfo());
            }
        } else if (radioMember.isSelected())
        {
            
            //search by memberID
            
            Rentee MemberFound = RS.SearchMember(Input);

            if (MemberFound == null)
            {
                txtMessage.setText("No such Member with the ID of \"" + Input + "\" in the data!");
            } else
            {
                txtMember.setText(MemberFound.getID());
                txtName.setText(MemberFound.getName());
                DefaultTableModel tableModel = (DefaultTableModel) tableLoan.getModel();

                tableModel.setRowCount(0);

                for (Comic comic : MemberFound.getComicsLoaned())
                {
                    Object[] rowData = new Object[4];
                    rowData[0] = comic.getISBN();
                    rowData[1] = comic.getTitle();
                    rowData[2] = comic.getRentalFee();
                    rowData[3] = comic.getDepositFee();
                    tableModel.addRow(rowData);
                }
            }

        } else
        {   
            //if no radio button is selected
            JOptionPane.showMessageDialog(null, "Please choose an option to search!", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnSearch1ActionPerformed

