    private void btnNextComicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextComicActionPerformed

        //to scroll to next comic
        
        String ISBN = txtISBN.getText();
        int index = RS.getComicIndex(ISBN);

        int size = RS.getSizeOfComic();
        if (index < size - 1)
        {
            index++;
            Comic displayComic = RS.searchComicByIndex(index);
            txtISBN.setText(displayComic.getISBN());
            txtTitle.setText(displayComic.getTitle());
            txtRentalCost.setText(displayComic.getRentalFee());
            txtDeposit.setText(displayComic.getDepositFee());
            txtInfo.setText(displayComic.getInfo());
            lblComic.setText("Comic " + (index + 1) + " of " + size);
        } else
        {
            JOptionPane.showMessageDialog(null, "You have reached the end of the list.");
        }


    }//GEN-LAST:event_btnNextComicActionPerformed

    private void btnNextMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextMemberActionPerformed
        
        //to scroll to next member
        
        String MemberID = txtMember.getText();
        int index = RS.getMemberIndex(MemberID);
        index++;
        int size = RS.getSizeOfMember();

        if (index >= size)
        {
            JOptionPane.showMessageDialog(null, "You have reached the end of this list.");
            return;
        }

        Rentee displayMember = RS.searchMemberByIndex(index);

        txtMember.setText(displayMember.getID());
        txtName.setText(displayMember.getName());

        DefaultTableModel tableModel = (DefaultTableModel) tableLoan.getModel();

        tableModel.setRowCount(0);

        for (Comic comic : displayMember.getComicsLoaned())
        {
            Object[] rowData = new Object[4];
            rowData[0] = comic.getISBN();
            rowData[1] = comic.getTitle();
            rowData[2] = comic.getRentalFee();
            rowData[3] = comic.getDepositFee();
            tableModel.addRow(rowData);
        }
        lblRentee.setText("Rentee " + (index + 1) + " of " + size);

    }//GEN-LAST:event_btnNextMemberActionPerformed

