        for (Comic comic : displayMember.getComicsLoaned())
        {
            Object[] rowData = new Object[4];
            rowData[0] = comic.getISBN();
            rowData[1] = comic.getTitle();
            rowData[2] = comic.getRentalFee();
            rowData[3] = comic.getDepositFee();
            tableModel.addRow(rowData);
        }

        lblRentee.setText("Rentee " + (index + 1) + " of " + Membersize);
